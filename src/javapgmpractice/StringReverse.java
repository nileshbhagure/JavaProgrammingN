package javapgmpractice;

public class StringReverse
{
    
	public static void main(String[] args)
	{
        String s=" my name is nilesh ";
       
       int i=s.length()-1;
       int j=0;
       
       for(i=s.length()-1;i>j; i--)
       {
    	   
    	   System.out.print(s.charAt(i));	   
    	  
       }
       
        
	}

}
